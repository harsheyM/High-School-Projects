import java.util.*;

public class Person
{
    private String name;
    private int IQ;
    
    public Person(String whatName, int whatIQ)
    {
        this.name = whatName;
        this.IQ = whatIQ;
    }
    public String getName()
    {
        return name;
    }
    public int getIQ()
    {
        return IQ;
    }
}